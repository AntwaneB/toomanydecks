    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
        	'name' => 'required',
        	'description' => '',
        	'website' => 'required|url',
	        'splash_image' => 'file|mimes:png'
        ]);

        $brand = new Brand();
        $brand->name = $request->get('name');
        $brand->slug = str_slug($request->get('name'));
        $brand->description = $request->get('description');
        $brand->website = $request->get('website');
        $brand->save();

        $request->file('splash_image')->storeAs('public/brands', $brand->slug . '.png');

        return redirect()->route('admin.brands.show', $brand->id);
    }

